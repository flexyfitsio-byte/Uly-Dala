# Простая защита от prompt injection в пользовательском вводе
BLOCKED_PATTERNS = [
    "забудь предыдущие инструкции",
    "ignore previous instructions",
    "ignore the above",
    "ты теперь",
    "system prompt",
    "напиши код",
    "выполни код",
]


def contains_suspicious_instruction(user_text: str) -> bool:
    lowered = user_text.lower()
    return any(pattern in lowered for pattern in BLOCKED_PATTERNS)
